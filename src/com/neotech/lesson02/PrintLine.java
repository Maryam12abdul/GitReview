package com.neotech.lesson02;

public class PrintLine {

	public static void main(String[] args) {
		// println prints the whole statement and moves the cursor to the next line
		System.out.println("What's up?");

		System.out.println("How are you doing?");

		// print prints the whole statement and keeps the cursor in the same line
		System.out.print("I am doing good!");

		System.out.print("1");
		System.out.print("2");
		System.out.print("3");
		System.out.print("4");

		System.out.println();

		// we can also use \n to insert a new line
		System.out.println("Do you like Java? \n Yes I do!");

		// the above statement is equal to
		System.out.println("Do you like Java?");
		System.out.println(" Yes I do!");

		System.out.print("\n");
		System.out.print("\n");
		System.out.print("\n");
		System.out.print("\n");
		System.out.print("\n");

		System.out.println("Hello there");

		System.out.println("\n");
		System.out.println("\n");

		System.out.print("\n\n\n\n");

		System.out.println("Bye now!");

	}

}
